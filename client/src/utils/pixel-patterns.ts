function makeCheckerPattern(a: string, b: string): PixelPattern {
  const rows: string[] = [];
  for (let y = 0; y < 16; y++) {
    let row = '';
    for (let x = 0; x < 16; x++) {
      row += (x + y) % 2 === 0 ? a : b;
    }
    rows.push(row);
  }
  return rows;
}

